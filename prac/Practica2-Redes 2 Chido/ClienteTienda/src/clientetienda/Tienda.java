    public void showCurItem() throws IOException{
        BufferedImage preIm = ImageIO.read(new File("imgs/"+prod[curItem].getImagen(0)));
        Image prodIm = preIm.getScaledInstance(jLabel1.getSize().width, jLabel1.getSize().height,Image.SCALE_SMOOTH);
        jLabel1.setIcon(new ImageIcon(prodIm));
        jLabel2.setText(prod[curItem].getNombre());
        jLabel3.setText(prod[curItem].getDescripcion());
        jLabel4.setText("Existencias: "+prod[curItem].getExistencia());
        jLabel5.setText("$"+prod[curItem].getPrecio());
    }
    
    public void showCurPhoto() throws IOException{
        BufferedImage preIm = ImageIO.read(new File("imgs/"+prod[curItem].getImagen(curIm)));
        Image prodIm = preIm.getScaledInstance(jLabel1.getSize().width, jLabel1.getSize().height,Image.SCALE_SMOOTH);
        jLabel1.setIcon(new ImageIcon(prodIm));
    }
    
    public void changePhoto(boolean back) throws IOException{
        if(back){
            if(curIm==0)
                curIm=prod[curItem].getImagenes().length-1;
            else
                curIm--;
        }
        else{
            if(curIm==prod[curItem].getImagenes().length-1)
                curIm=0;
            else
                curIm++;
        }
        showCurPhoto();
    }
    
    public void changeItem(boolean back) throws IOException{
        if(back){
            if(curItem==0)
                curItem=prod.length-1;
            else
                curItem--;
        }
        else{
            if(curItem==prod.length-1)
                curItem=0;
            else
                curItem++;
        }
        showCurItem();
    }

