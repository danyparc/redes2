    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
                // TODO add your handling code here:
        String lisnombres="<html>", liscant="<html>",lisImporte="<html>";
        float tot=0;
        for (Pedido tem : pedi) {
            float preci=0;
            for (Producto prod1 : prod) {
                if(prod1.getId()==tem.id){
                    preci=prod1.getPrecio();
                    lisnombres+=prod1.getNombre()+"<br>";
                    break;
                }
            }
            liscant+=tem.cantidad+"<br>";
            lisImporte+=(tem.cantidad*preci)+"<br>";
            tot+=tem.cantidad*preci;
        }
        lisnombres+="</html>";
        liscant+="</html>";
        lisImporte+="</html>";
        
        jLabel8.setText(lisnombres);
        jLabel7.setText(liscant);
        jLabel6.setText(lisImporte);
        jLabel12.setText("Total: $"+tot);
        jDialog3.setVisible(true);
    }//GEN-LAST:event_jButton8ActionPerformed

